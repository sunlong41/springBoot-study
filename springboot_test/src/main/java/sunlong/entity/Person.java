package sunlong.entity;

public class Person {
    private String aa;
    private String bb;

    public Person() {
    }

    public Person(String aa, String bb) {
        this.aa = aa;
        this.bb = bb;
    }

    public String getAa() {
        return aa;
    }

    public void setAa(String aa) {
        this.aa = aa;
    }

    public String getBb() {
        return bb;
    }

    public void setBb(String bb) {
        this.bb = bb;
    }
}
